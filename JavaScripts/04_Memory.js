/*
 Types of Memory in JavaScript
 JavaScript manages memory in two main areas: 
 1. Stack Memory
 2. Heap Memory

 Stack Memory
 - Stores primitive values (numbers, strings, booleans)
 - Managed automatically (LIFO)
 - Faster access
 - Stores a copy of a's value.


 Heap Memory
 - Stores reference types (objects, arrays, functions)
 - Managed manually (garbage collection)
 - Slower access
 - Stores a reference to the object's location in memory

*/